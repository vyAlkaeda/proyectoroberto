    fun buscarEnfermedadesPorSintomas(sintomasSeleccionados: List<String>): List<String> {
        return diseases.filter { disease ->
            sintomasSeleccionados.any { sintoma ->
                disease.symptoms.any { enfermedadSintoma ->
                    enfermedadSintoma.contains(sintoma, ignoreCase = true) ||
                    sintoma.contains(enfermedadSintoma, ignoreCase = true)
                }
            }
        }.map { it.name }
    }

    fun buscarEnfermedadesConContador(sintomasSeleccionados: List<String>): Map<String, Int> {
        return diseases.mapNotNull { disease ->
            val count = sintomasSeleccionados.count { sintoma ->
                disease.symptoms.any { enfermedadSintoma ->
                    enfermedadSintoma.contains(sintoma, ignoreCase = true) ||
                    sintoma.contains(enfermedadSintoma, ignoreCase = true)
                }
            }
            if (count > 0) disease.name to count else null
        }.sortedByDescending { it.second }
         .toMap()
    }
